/*
Objective 
Use loops to do some simple math.

Task 
Given an integer, N , print its first 10 multiples. 
Each multiple N x i (where 1<=i<=10) should be printed on a new line in the form: N x i = result.

Input Format
A single integer, N.
Constraints

Output Format
Print 10 lines of output; each line i (where 1<=i<=10) contains the result of i in the form: 
N x i = result.

*/

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.io.IOException;
public class SimpleLoop{
	public static void main(String[]args) throws IOException{
		BufferedReader br=new BufferedReader(new InputStreamReader(System.in));
		int N=Integer.parseInt(br.readLine());
		for (int i=1;i<=10;i++) {
			System.out.println(N+" x "+i+" = "+N*i);
		}
		
	}
}
